import 'package:flutter/material.dart';

font2xl(String text, {Color? color}) {
  return Text(
    text,
    style: TextStyle(
      fontSize: 20,
      color: color,
      fontFamily: 'RobotoSlab',
    ),
    overflow: TextOverflow.ellipsis,
    maxLines: 1,
  );
}

